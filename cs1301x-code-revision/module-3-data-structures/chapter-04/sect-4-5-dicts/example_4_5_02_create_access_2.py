# Creates myDictionary with key-values below

myDictionary = {
   "sprockets" : 5,
   "widgets": 11,
   "cogs" : 3,
   "gizmos": 15
}
print(myDictionary)
# Output is {'sprockets': 5, 'widgets': 11, 'cogs': 3, 'gizmos': 15}

myDictionary["sprockets"] += 1
print(myDictionary)
# Output is {'sprockets': 6, 'widgets': 11, 'cogs': 3, 'gizmos': 15}
